function inchesToFeet(inches)
{
  return(inches/12);
}

function inchesToYards(inches)
{
  return(inches/36);
}

function feetToInches(feet)
{
  return(feet*12);
}

function feetToYards(feet)
{
  return(feet/3);
}

function yardsToInches(yards)
{
  return(yards*36);
}

function yardsToFeet(yards)
{
  return(yards*12);
}
